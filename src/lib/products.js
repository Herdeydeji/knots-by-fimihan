export const getProductBySlug = (slug) => products.find((p) => p.slug === slug)
export const getProductById = (id) => products.find((p) => p.id === id)
export const getProductsByCategory = (category) =>
  products.filter((p) => p.category === category && p.isActive)
export const getFeaturedProducts = () =>
  products.filter((p) => p.isFeatured && p.isActive)
export const getRelatedProducts = (product) =>
  products.filter((p) => p.category === product.category && p.id !== product.id && p.isActive).slice(0, 4)
export const searchProducts = (query) => {
  const q = query.toLowerCase()
  return products.filter(
    (p) =>
      p.isActive &&
      (p.name.toLowerCase().includes(q) ||
        p.description.toLowerCase().includes(q) ||
        p.tags.some((t) => t.toLowerCase().includes(q)) ||
        p.category.toLowerCase().includes(q))
  )
}
